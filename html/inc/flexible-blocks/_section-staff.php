                    <h3>Current Projects</h3>
                    <div class="spacer-48 spacer-xs-24"></div>
                    <h5>Transfusion Immunology</h5>
                    <div class="spacer-16"></div>
                    <p>Red blood cell (RBC) transfusions remain a very important modality of treatment for patients with hemoglobinopathies including sickle cell disease (SCD).  Despite its benefits, RBC transfusion can result in the development of antibodies (alloimmunization) against transfused cells in patients with SCD and thalassemia. Alloantibodies can cause delayed hemolytic transfusion reactions which in SCD patients can trigger hyperhemolysis, a life-threatening poorly understood phenomena in which the transfused and the patient’s own RBCs are destroyed. In addition, finding compatible units for patients with alloantibodies can be difficult and identifying and characterizing the antibodies can be costly, time-consuming and laborious, causing transfusion delays. This highlights the need for better characterization of triggers of alloimmunization and identification of risk factors. We are studying cellular immune responses to transfused blood products with the goal of developing strategies to prevent immune-mediated transfusion reactions.  Current research focus is on characterizing immunoregulatory networks, including defective regulatory T and B cell pathways that result in alloimmunization.  In addition, we have identified a novel subset of follicular T cells (Tfh) critical in providing help to B cells for the generation of antibody responses. The activity of this Tfh subset, which expresses the checkpoint inhibitor molecule TIGIT, differs in patients with and without alloantibodies.  We are currently examining how abnormal TIGIT signaling may lead to alloimmunization with the goal that this information may help toward future identification of biomarkers of alloimmunization and therapeutic strategies for its prevention.</p>
                    <div class="spacer-24"></div>
                    <h5>Immunological response to hemolysis</h5>
                    <div class="spacer-16"></div>
                    <p>Patients with hemolytic anemias including patients with SCD suffer from ongoing intravascular hemolysis, which results in the release of free hemoglobin and heme into the circulation, causing activation of the underlying vasculature.  We have identified aberrant immune response to free heme inalloimmunized patients with SCD and we are currently investigating immunological response to hemolysis in the setting of transfusions focusing on HO-1 and TLR-4 mediated pathways that can be manipulated to prevent and even reverse alloimmunization.  Furthermore, painful vaso-occlusive crisis (VOC), a clinical hallmark of SCD, resulting from obstruction of vasculature by sickle RBCs is associated with microvascular damage. Currently, the molecular mechanisms resulting in VOC remain incompletely understood. We have identified a novel function of patrolling monocytes in the removal of hemolysis-injured endothelial cells in the SCD vasculature and in lowering VOC risk. Current studies are focused on understanding the molecular machinery involved in the vasculoprotective role of patrolling monocytes and the therapeutic potential of manipulating this subset in SCD.</p>
                    <div class="spacer-24"></div>
                    <h5>Platelet Autoimmunity</h5>
                    <div class="spacer-16"></div>
                    <p>We are characterizing the cellular and molecular pathways resulting in platelet autoimmunity in patients with immune thrombocytopenia (ITP) with the goal of developing new and improved treatments. To date, our studies have unraveled quantitative and functional differences in monocytes and their control of T effector functions with possible involvement of ITP platelets in driving autoimmunity. Current research focus is on identifying immunological signatures of ITP drug response.</p>
